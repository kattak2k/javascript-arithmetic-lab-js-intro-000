function add(a,b){
  return a + b;
}

function subtract(a,b){
  return a - b;
}
function multiply(a,b){
  var result = a * b
  return result;
}
function divide(a,b){
  return a / b;
}

function increment(a){
  return a + 1;
}

function decrement(a){
  return a - 1;
}

function makeInt(n){
  return parseInt("10", n);
}

function preserveDecimal(n){
  return parseFloat(n);
}
